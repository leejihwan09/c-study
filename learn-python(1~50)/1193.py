a=int(input())
a-=1
c=0
r1=1
r2=1
while c<a:
    if r2==1:
        r2+=1
        c+=1
        if c>=a:
            break
        c+=1
        r1,r2=r2,r1
        if c>=a:
            break
    if r1!=1:
        c+=1
        r1+=1
        if c>=a:
            break
print(f'{r1}/{r2}')